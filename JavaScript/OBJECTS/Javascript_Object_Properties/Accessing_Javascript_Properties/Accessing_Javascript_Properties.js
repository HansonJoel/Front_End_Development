// You can access javascript object properties using any of the methods below

const person = {
    firstname: "John",
    lastname: "Doe",
    age: 50,
    eyecolor: "blue"
}


 // Using objectName.property synatax
let age = person.age;    
console.log(age);


// using objectName["property"]
let firstname = person["firstname"];   

//using objectName[expression]
let lastname = person[lastname];